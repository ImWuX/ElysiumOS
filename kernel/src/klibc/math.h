#ifndef KLIBC_MATH_H
#define KLIBC_MATH_H

/**
 * @brief Get the absolute value
 *
 * @param n The non-absolute value
 * @returns The absolute value
 */
int abs(int n);

/**
 * @brief Get the max of two values
 *
 * @param a Value a
 * @param b Value b
 * @returns The maximum value
 */
int max(int a, int b);

/**
 * @brief Get the min of two values
 *
 * @param a Value a
 * @param b Value b
 * @returns The minimum value
 */
int min(int a, int b);

#endif